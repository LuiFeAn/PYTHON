# STRING

string = 'Olá, mundo'

# Encontrar substring

find_string_result = string.find('mundo');

# Substituindo string

replace_string_result = string.replace('mundo','world');


# Convertendo caracteres da string para maísculas

upper_string_result = string.upper();

# Convertendo caracteres da string para minúsculas

lower_string_result = string.lower();

# Converte a primeira letra de cada palavra da string para maíscula

title_string_result = string.title()

string_two = '     OLA, MUNDO     '

# Remove espações em branco do ínicio e do fim da string
strip_string_result = string_two.strip()


print({
    string,
    find_string_result,
    replace_string_result,
    upper_string_result,
    lower_string_result,
    title_string_result,
    strip_string_result,
})

